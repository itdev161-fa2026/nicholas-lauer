import mongoose from "mongoose";

const UserScheme = new mongoose.SchemaType({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
});

const User = mongoose.model('user', UserScheme);

export default User;